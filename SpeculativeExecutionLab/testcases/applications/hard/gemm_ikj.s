    movz x3, #64
    movz x4, #64
    movz x5, #64
    // call gemm_ikj
	sub sp, sp, #16
	stur x30, [sp]
    bl gemm_ikj
    // return and print state
	ldur x30, [sp]
	add sp, sp, #16
	ret
