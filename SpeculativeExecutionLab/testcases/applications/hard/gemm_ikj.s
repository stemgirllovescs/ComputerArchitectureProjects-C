gemm_ikj:
    //                     x0       x1       x2        x3        x4        x5
    //  void gemm_ikj(double *A, double *B, double *C, size_t m, size_t n, size_t p) {
    //      for (size_t i = 0; i < m; i++) {
    //          for (size_t k = 0; k < n; k++) {
    //              for (size_t j = 0; j < p; j++) {
    //                  C[i*n + j] += A[i*n + k] * B[k*p + j];
    //              }
    //          }
    //      }
    //  }
    // this is a leaf procedure so no stack frame needed
    eor x6, x6, x6      // i = 0
    eor x9, x9, x9      // i*n = 0
    b .L_test1
    .L_loop1:
    eor x8, x8, x8      // k = 0
    eor x10, x10, x10   // k*p = 0
    b .L_test2
    .L_loop2:
    eor x7, x7, x7      // j = 0
    b .L_test3
    .L_loop3:
    adds x11, x9, x8    // i*n + k
	lsl x11, x11, #3	// (i*n + k)*sizeof(long)
    adds x11, x0, x11   // (A + i*n + k)*sizeof(long)
    ldur x12, [x11]     // a = A[i*n + k]
    adds x11, x10, x7   // k*p + j
	lsl x11, x11, #3	// (k*p + j)*sizeof(long)
    adds x11, x1, x11   // B + (k*p + j)*sizeof(long)
    ldur x13, [x11]     // b = B[k*p + j]
    // loop for multiplication
    eor x14, x14, x14   // total = 0
    b .L_test4
    .L_loop4:
    adds x14, x14, x12  // total += a
    sub x13, x13, #1    // b--
    .L_test4:
    tst x13, x13		// b != 0?
    bne .L_loop4
    adds x11, x9, x7    // i*n + j
	lsl x11, x11, #3	// (i*n + j)*sizeof(long)
    adds x11, x2, x11   // C + (i*n + j)*sizeof(long)
	ldur x15, [x11]		// temp = C[i*n + j]
	adds x14, x14, x15	// total += temp
    stur x14, [x11] 	// C[i*n + j] = total
    add x7, x7, #1      // j++
    .L_test3:
    cmp x7, x5          // j < p?
    blt .L_loop3
    add x8, x8, #1      // k++
    adds x10, x10, x5   // k*p += p
    .L_test2:
    cmp x8, x4          // k < n?
    blt .L_loop2
    add x6, x6, #1      // i++
    adds x9, x9, x4     // i*n += n
    .L_test1:
    cmp x6, x3          // i < m?
    blt .L_loop1    
    ret
