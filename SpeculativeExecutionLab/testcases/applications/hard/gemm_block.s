    movz x1, #0x8000
	adds x2, x1, x1
	adds x1, x0, x1
    // load C matrix, destination, all zeros
    adds x2, x0, x2
    // parameters m, n, p, block
    movz x3, #64
    movz x4, #64
    movz x5, #64
    movz x6, #4
    // call gemm_block
	sub sp, sp, #16
	stur x30, [sp]
    bl gemm_block
