    //  void gemm_block(double *A, double *B, double *C, size_t m, size_t n, size_t p, size_t block) {
    //      for (size_t i = 0; i < m; i+=block) {
    //          for (size_t k = 0; k < n; k+=block) {
    //              for (size_t j = 0; j < p; j+block) {
    //                  for (size_t b_i = i; b_i < i+block && b_i < m; b_i++) {
    //                      for (size_t b_k = k; b_k < k+block && b_k < n; b_k++) {
    //                          for (size_t b_j = j; b_j < j+block && b_j < p; b_j++) {
    //                              C[b_i*n + b_j] += A[b_i*n + b_k] * B[b_k*p + b_j];
    //                          }
    //                      }
    //                  }
    //              }
    //          }
    //      }
    //  }

    // we're gonna need more registers, 
    // push a few nonvolatile ones on the stack
    sub sp, sp, #96
    stur x19, [sp]
    stur x20, [sp, #8]
    stur x21, [sp, #16]
    stur x22, [sp, #24]
    stur x23, [sp, #32]
    stur x24, [sp, #40]
    stur x25, [sp, #48]
    stur x26, [sp, #56]
    stur x27, [sp, #64]
    stur x28, [sp, #72]
    stur x29, [sp, #80]
    stur x30, [sp, #88] // there were exectly enough registers

    // calculate block*n and block*p,
    // we'll need those later
    eor x28, x28, x28   // block*n = 0
    eor x29, x29, x29   // block*p = 0
    eor x30, x30, x30   // counter = 0
    .L_loop8:
    adds x28, x28, x4   // block*n += n
    add x30, x30, #1    // counter++
    cmp x30, x6         // counter < block?
    blt .L_loop8
    eor x30, x30, x30   // counter = 0
    .L_loop9:
    adds x29, x29, x5   // block*p += p
    add x30, x30, #1    // counter++
    cmp x30, x6         // counter < block?
    blt .L_loop9

    // start of outermost loop
    eor x7, x7, x7      // i = 0
    eor x8, x8, x8      // i*n = 0
    b .L_test1
    .L_loop1:
    eor x9, x9, x9      // k = 0
    eor x10, x10, x10   // k*p = 0
    b .L_test2
    .L_loop2:
    eor x11, x11, x11   // j = 0
    b .L_test3
    .L_loop3:
    add x12, x7, #0     // b_i = i
    adds x13, x7, x6    // i+block (loop end)
    add x14, x8, #0     // b_i*n = i*n
    b .L_test4
    .L_loop4:
    add x15, x9, #0     // b_k = k
    adds x19, x9, x6    // k+block (loop end)
    add x20, x10, #0    // b_k*p = k*p
    b .L_test5
    .L_loop5:
    add x21, x11, #0    // b_j = j
    //forgot this so that's why its x27 and looks out of place lol
    adds x27, x11, x6   // j+block (loop end) 
    b .L_test6
    .L_loop6:
    adds x22, x14, x15  // b_i*n + b_k
	lsl x22, x22, #3	// (b_i*n + b_k)*sizeof(long)
    adds x22, x0, x22   // (A + b_i*n + b_k)*sizeof(long)
    ldur x23, [x22]     // a = A[b_i*n + b_k]
    adds x22, x20, x21  // b_k*p + b_j
	lsl x22, x22, #3	// (b_k*p + b_j)*sizeof(long)
    adds x22, x1, x22   // B + (b_k*p + b_j)*sizeof(long)
    ldur x24, [x22]     // b = B[b_k*p + b_j]
    // loop for multiplication
    eor x25, x25, x25   // total = 0
    b .L_test7
    .L_loop7:
    adds x25, x25, x23  // total += a
    sub x24, x24, #1    // b--
    .L_test7:
    tst x24, x24		// b != 0?
    bne .L_loop7
    // end loop
    adds x22, x14, x21  // b_i*n + b_j
	lsl x22, x22, #3	// (b_i*n + b_j)*sizeof(long)
    adds x22, x2, x22   // C + (b_i*n + b_j)*sizeof(long)
	ldur x26, [x22]		// temp = C[b_i*n + b_j]
	adds x25, x25, x26	// total += temp
    stur x25, [x22] 	// C[i*n + j] = total
    add x21, x21, #1    // b_j++
    .L_test6:
    cmp x21, x27        // b_j < j+block?
    // should technically be uncommented, but we get away with it
    // because the matrices are all square 
    // and dimensions are divisible by block
    //bge .L_done6
    //cmp x21, x5         // b_j < p?
    blt .L_loop6
    .L_done6:
    add x15, x15, #1    // b_k++
    adds x20, x20, x5   // b_k*p += p
    .L_test5:
    cmp x15, x19        // b_k < k+block?
    //bge .L_done5
    //cmp x15, x5         // b_k < n?
    blt .L_loop5
    .L_done5:
    add x12, x12, #1    // b_i++
    adds x14, x14, x4   // b_i*n += n
    .L_test4:
    cmp x12, x13        // b_i < i+block?
    //bge .L_done4
    //cmp x12, x3         // b_i < m?
    blt .L_loop4
    .L_done4:
    adds x11, x11, x6   // j += block
    .L_test3:
    cmp x11, x5         // j < p?
    blt .L_loop3
    adds x9, x9, x6     // k += block
    adds x10, x10, x29  // k*p += block*p
    .L_test2:
    cmp x9, x4          // k < n?
    blt .L_loop2
    adds x7, x7, x6     // i += block
    adds x8, x8, x28    // i*n += block*n
    .L_test1:
    cmp x7, x3          // i < m?
    blt .L_loop1
    // end of outermost loop
    // restore nonvolatile registers
    ldur x19, [sp]
    ldur x20, [sp, #8]
    ldur x21, [sp, #16]
    ldur x22, [sp, #24]
    ldur x23, [sp, #32]
    ldur x24, [sp, #40]
    ldur x25, [sp, #48]
    ldur x26, [sp, #56]
    ldur x27, [sp, #64]
    ldur x28, [sp, #72]
    ldur x29, [sp, #80]
    ldur x30, [sp, #88]
    add sp, sp, #96
    ret
